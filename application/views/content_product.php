		$('.product2_btn_contain>div').eq(this_index).show().siblings().hide();
	})
	$('.product2_btn_descript_img').mouseenter(function(){
		var v2 = $(this).children('.product2_btn_descript_img_word').height();
		$(this).children('.product_mask').stop().show();
		$(this).children('.product2_btn_descript_img_word').stop().animate({'height':v2,'margin-bottom':-v2/2},300).slideDown();
	})
	$('.product2_btn_descript_img').mouseleave(function(){
		var v2 = $(this).children('.product2_btn_descript_img_word').height();
		$(this).children('.product_mask').stop().hide();
		$(this).children('.product2_btn_descript_img_word').slideUp();
	})
})
